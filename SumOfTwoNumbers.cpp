/*
	Sum of two Numbers program: 
    
    A program that stores integers 50 and 100 in variables
    and stores the sum of these two in a variable named total
*/


#include <iostream> 
using namespace std;
int main()
{
    int var1, var2, total;

    var1 = 50, var2 = 100;
    total = var1 + var2;

    return 0;
}
